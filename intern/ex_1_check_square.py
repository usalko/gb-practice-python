import ast

print('Упражнение 1. По двум заданным числам проверять является ли первое квадратом второго')
val1 = ast.literal_eval(input('Введите первое число: '))
val2 = ast.literal_eval(input('Введите второе число: '))
if val1 == val2**2:
    print('Первое число является квадратом второго числа')
else:
    print('Первое число не является квадратом второго числа')
