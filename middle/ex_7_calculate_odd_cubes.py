import ast

print('Упражнение 7. Показать кубы чисел, заканчивающихся на четную цифру')
# Ввод и определение типа
n = ast.literal_eval(input('Введите число: '))
if not isinstance(n, int):
    print(f'Извините, не могу показать кубы чисел . {n} не является целым числом.')
else:
    print(f'Кубы чисел которые заканчиваются на четную цифру {", ".join([str(a) + "**3 = " + str(a**3) for a in range(1, n + 1) if a**3 % 2 == 0 ])}')
